package kr.co.AMS.Model.vo;

public class APTCalendar_Dto {
	private int cal_idx;
	private String title;
	private String start_cal;
	private String end_cal;

	public int getCal_idx() {
		return cal_idx;
	}

	public void setCal_idx(int cal_idx) {
		this.cal_idx = cal_idx;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getStart_cal() {
		return start_cal;
	}

	public void setStart_cal(String start_cal) {
		this.start_cal = start_cal;
	}

	public String getEnd_cal() {
		return end_cal;
	}

	public void setEnd_cal(String end_cal) {
		this.end_cal = end_cal;
	}
}
